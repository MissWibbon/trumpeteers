function randomizeArr(arr) {
    const newArr = [];
    const copy = arr.map(item => item);

    while (copy.length > 0) {
        const randomIndex = Math.floor(Math.random() * copy.length);
        newArr.push(copy[randomIndex]);
        copy.splice(randomIndex, 1);
    }
    return newArr;
}



